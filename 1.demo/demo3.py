#  4:00000100
#  8:00001000
#  &(按位与):有0则0
print(4&8)  #  0
#  |(按位或)：有1则1
print(4|8)  #  1100-->12
#  <<(左移):高位溢出舍弃，低位补0  左移一位相当于乘以2
print(4<<2)  #  00010000-->16
#  >>(右移)：低位溢出舍弃，高位补0  右移一位相当于除以2
print(4>>2)  #  00000001-->1

