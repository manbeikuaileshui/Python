"""
python一切皆对象，所有对象都有一个布尔值
获取对象的布尔值使用内置函数boll()
"""
print('----------以下对象的布尔值为Flase----------')
print('Flase',bool(False))  #  False
print('0',bool(0))  #  数值0
print('0.0',bool(0.0))  #  数值0.0
print('None',bool(None))  #  None
print('',bool(''))  #  空字符串
print('[]',bool([]))  #  空列表
print('list()',bool(list()))  #  空列表
print('()',bool(()))  #  空元组
print('tuple()',bool(tuple()))  #  空元组
print('{}',bool({}))  #  空字典
print('dic()',bool(dict()))  #  空字典
print('set()',bool(set()))  #  空集合
print('----------其它对象的布尔值均为True---------')

